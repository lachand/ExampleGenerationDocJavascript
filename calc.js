/**
 * Substract two nubers.
 * @constructor
 * @param {float} a the lef number
 * @param {float} b - the right number
 * @return the result of a - b
 */
function substract(a,b){
    return a-b
}

/**
 * Add two nubers.
 * @constructor
 * @param {float} a the lef number
 * @param {float} b - the right number
 * @return the result of a + b
 */
function add(a,b){
    return a+b;
}
